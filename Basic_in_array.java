package Array;

public class Basic_in_array {
    public static void main(String[] args) {
        int [] arr = {5,4,45,78,9,35,25};   // indexing
        // access
        System.out.println(arr[0]);
        // updating element or mutability
        arr[3]=27;
        System.out.println(arr[3]);

        int [] x= new int[4];  // 4 size ka array
        x[0]=46;
        x[1]=41;
        x[2]=51;
        x[3]=65;
    }
}
